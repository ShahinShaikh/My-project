package com.techcanvass.day1;

public class ArithmeticOperation {
	public int addition(int addnum1,int addnum2)
	{
		int sum = addnum1 + addnum2;
		return sum;
	}
	public int subtraction(int subnum1,int subnum2)
	{
		int diff = subnum1 - subnum2;
		return diff;
	}
	public int multiplication(int mulnum1,int mulnum2)
	{
		int mul = mulnum1 * mulnum2;
		return mul;
	}
	public int division(int divnum1,int divnum2)
	{
		int div = divnum1 / divnum2;
		return div;
	}
	public int remainder(int remnum1,int remnum2)
	{
		int rem = remnum1 % remnum2;
		return rem;
	}
}
